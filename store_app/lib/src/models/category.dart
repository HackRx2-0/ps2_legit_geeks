class Category {
  String id = UniqueKey().toString();
  String name;
  bool selected;
  IconData icon;
  List<Product> products;
  List<SubCategory> subCategories;
  Category({this.name, this.icon, this.selected, this.products});

  Category.fromJson(Map<String, dynamic> res) {
    this.id = res['id'].toString();
    this.name = res['name'];
    this.icon = UiIcons.iconMapper[res['icon']];
    this.selected = false;
    if (res['sub_categories'] != null) {
      subCategories = [];
      for (var x in res['sub_categories']) {
        SubCategory subCategory = SubCategory();
        subCategory.fromJson(x);
        subCategories.add(subCategory);
      }
    }
    if (res['products'] != null) {
      products = [];
      for (var x in res['products']) {
        Product product = Product.fromJson(x);
        products.add(product);
      }
    }
  }
}

class SubCategory {
  String id = UniqueKey().toString();
  String name;
  bool selected;
  List<Product> products;

  SubCategory({this.name, this.selected, this.products});
  void fromJson(Map<String, dynamic> data) {
    this.id = data['id'].toString();
    this.name = data['name'];
    if (data['products'] != null) {
      products = [];
      for (var x in data['products']) {
        Product product = Product.fromJson(x);
        products.add(product);
      }
    }
  }
}

